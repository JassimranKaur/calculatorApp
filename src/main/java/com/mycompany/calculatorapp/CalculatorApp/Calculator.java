public class Calculator {
    private double a;
    private double b;
    
    public Calculator(){
        this.a = a;
        this.b = b;
    }
    
    public double addition(){
        double result = a+b;
        return result;
    }


    public double getA() {
        return a;
    }


    public void setA(double a) {
        this.a = a;
    }


    public double getB() {
        return b;
    }


    public void setB(double b) {
        this.b = b;
    }
}
